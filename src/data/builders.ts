export function findBuilder(handle: string): Builder | undefined {
  return BUILDERS.find((b) => b.handle.toLowerCase() === handle.toLowerCase());
}

export function buildersByCategory(): Record<Category, Builder[]> {
  const map = {} as Record<Category, Builder[]>;
  for (const c of CATEGORIES) map[c.id] = [];
  for (const b of BUILDERS) map[b.category].push(b);
  return map;
}
